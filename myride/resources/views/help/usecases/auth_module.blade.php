<h2>Auth Module</h2><hr>
<h3>How to Login?</h3>
<p>To sign it to MyRide Apps you must provide <b class="text-primary">Email / Username</b> and <b class="text-primary">Password</b>.</p>
<h5 class="fw-bold mt-3">Via Basic Auth</h5>
<ol>
    <li>In the Login Page, find the login form that contain <b class="text-primary">Email / Username</b> and <b class="text-primary">Password</b>.</li>
    <li>Fill the form and press the <b class="text-success">Enter the Garage</b> button to verify your login.</li>
</ol>
<h5 class="fw-bold mt-3">Via Google Sign-In</h5>
<ol>
    <li>In the Login Page, find the login form that contain <b class="text-primary">Email / Username</b> and <b class="text-primary">Password</b>.</li>
    <li>Press the <b class="text-success">Sign-In With Google</b> button to be redirected to the Google Sign-In page.</li>
    <li>Choose your Gmail Account, read the terms & conditions, and click <b class="text-success">Continue</b> on the Google Sign Page. The process of Google Account signin
    finish after you have been redirect to Dashboard Page with welcoming message.</li>
</ol>
<h3>How to Sign Out?</h3>
<ol>
    <li>In the Every Page, you can press the button with icon like <b class="text-primary">Exit</b> menu.</li>
    <li>It will show you the validation of Sign-Out Process using Pop-Up, find and click the <b class="text-primary">Sign Out</b> button.</li>
    <li>A popup of recover confirmation will appear, click the button <b class="text-success">Yes, Sign Out</b> to proceed.</li>
</ol>
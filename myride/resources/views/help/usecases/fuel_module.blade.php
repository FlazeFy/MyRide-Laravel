<h2>Fuel Module</h2><hr>
<h3>How to Add Fuel?</h3>
<p>To add your past fuel history at MyRide Apps you must select your <b class="text-primary">Vehicle</b>, provide <b class="text-primary">Fuel Brand</b>, <b class="text-primary">Fuel Type</b>, <b class="text-primary">Fuel RON</b>, 
<b class="text-primary">Volume (L)</b>, and <b class="text-primary">Total Price</b>. You can also provide the <b class="text-primary">Fuel Receipt</b> capture as image with format such as PNG, JPG, or JPEG.</p>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Fuel</b>, after that you will be redirect to Fuel Page.</li>
    <li>At the Fuel Page, press button <b class="text-success">+ Fuel</b>.</li>
    <li>Fill the form and also you can add your fuel purchase receipt by pressed the <b class="text-success">Add Receipt</b> button.</li>
    <li>Press the <b class="text-success">Save Fuel</b> button to finish.</li>
</ol>
<h3>How to See Your Fuel History?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Fuel</b>, after that you will be redirect to Fuel Page.</li>
    <li>Your fuel history is displayed in a table format.</li>
    <li>To see the fuel purchase receipt that you have upload before, you can press the button that have "<b class="text-success">Receipt</b>" icon. A pop-up will appear that showing your fuel receipt.</li>
</ol>
<h3>How to Edit Your Fuel History?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Fuel</b>, after that you will be redirect to Fuel Page.</li>
    <li>Your fuel history is displayed in a table format.</li>
    <li>To see the fuel edit form, you can press the button that have "<b class="text-success">Edit</b>" icon. An edit pop-up will appear.</li>
    <li>Change the field that you want to modify. And press the <b class="text-success">Save Changes</b> button to submit changes.</li>
</ol>
<h3>How to Delete Your Fuel History?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Fuel</b>, after that you will be redirect to Fuel Page.</li>
    <li>Your fuel history is displayed in a table format.</li>
    <li>After you find the fuel history that you want to delete. Press the button that have "<b class="text-success">Trash</b>" icon.</li>
    <li>A validation pop-up will appear, press <b class="text-success">Yes, delete it</b> button to finally delete your fuel history.</li>
</ol>
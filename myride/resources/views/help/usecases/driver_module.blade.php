<h2>Driver Module</h2><hr>
<h3>How to Add Driver?</h3>
<p>To set a driver at MyRide Apps you must provide <b class="text-primary">Fullname</b>, <b class="text-primary">Username</b>, <b class="text-primary">Email</b>, <b class="text-primary">Telegram ID</b>, 
<b class="text-primary">Phone</b>, and <b class="text-primary">Password</b>.</p>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Driver</b>, after that you will be redirect to Driver Page.</li>
    <li>At the Driver Page, press button <b class="text-success">+ Driver</b>. And it will redirect you to the Add Driver Page</li>
    <li>Fill the form and press the <b class="text-success">Save Driver</b> button to finish.</li>
</ol>
<h3>How to See Your Driver?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Driver</b>, after that you will be redirect to Driver Page.</li>
    <li>Your driver is displayed in a table format.</li>
</ol>
<h3>How to Edit Your Driver?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Driver</b>, after that you will be redirect to Driver Page.</li>
    <li>Your driver is displayed in a table format.</li>
    <li>To see the driver edit form, you can press the button that have "<b class="text-success">Edit</b>" icon. An edit pop-up will appear.</li>
    <li>Change the field that you want to modify. And press the <b class="text-success">Save Changes</b> button to submit changes.</li>
</ol>
<h3>How to Delete Your Driver?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Driver</b>, after that you will be redirect to Driver Page.</li>
    <li>Your driver is displayed in a table format.</li>
    <li>After you find the driver that you want to delete. Press the button that have "<b class="text-success">Trash</b>" icon.</li>
    <li>A validation pop-up will appear, press <b class="text-success">Yes, delete it</b> button to finally delete your driver.</li>
</ol>
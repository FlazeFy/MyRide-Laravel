<h2>Reminder Module</h2><hr>
<h3>How to Add Reminder?</h3>
<p>To set a reminder at MyRide Apps you must select your <b class="text-primary">Vehicle</b>, provide <b class="text-primary">Reminder Context</b>, <b class="text-primary">Remind At</b>, <b class="text-primary">Reminder Title</b>, 
<b class="text-primary">Reminder Body</b>, and <b class="text-primary">Reminder Attachment</b>. For the attachment, you can add an <b class="text-primary">Reminder Image</b> with JPG, PNG, or JPEG format. And a <b class="text-primary">Reminder Location</b>.</p>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Reminder</b>, after that you will be redirect to Reminder Page.</li>
    <li>At the Reminder Page, press button <b class="text-success">+ Reminder</b>. And it will redirect you to the Add Reminder Page</li>
    <li>To add an image or location, you can press <b class="text-success">Add Image</b> button or <b class="text-success">Add Location</b> to open location Map. Select location using marker or just directly type the coordinate.</li>
    <li>Fill the form and press the <b class="text-success">Save Reminder</b> button to finish.</li>
</ol>
<h3>How to See Your Reminder?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Reminder</b>, after that you will be redirect to Reminder Page.</li>
    <li>Your reminder is displayed in a table format.</li>
</ol>
<h3>How to Edit Your Reminder?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Reminder</b>, after that you will be redirect to Reminder Page.</li>
    <li>Your reminder is displayed in a table format.</li>
    <li>To see the reminder edit form, you can press the button that have "<b class="text-success">Edit</b>" icon. An edit pop-up will appear.</li>
    <li>Change the field that you want to modify. And press the <b class="text-success">Save Changes</b> button to submit changes.</li>
</ol>
<h3>How to Delete Your Reminder?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Reminder</b>, after that you will be redirect to Reminder Page.</li>
    <li>Your reminder is displayed in a table format.</li>
    <li>After you find the reminder that you want to delete. Press the button that have "<b class="text-success">Trash</b>" icon.</li>
    <li>A validation pop-up will appear, press <b class="text-success">Yes, delete it</b> button to finally delete your reminder.</li>
</ol>
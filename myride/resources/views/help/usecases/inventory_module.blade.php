<h2>Inventory Module</h2><hr>
<h3>How to Add Inventory?</h3>
<p>To set a inventory at MyRide Apps you must select your <b class="text-primary">Vehicle</b>, provide <b class="text-primary">Inventory Category</b>, <b class="text-primary">Inventory Storage</b>, <b class="text-primary">Qty</b>, 
<b class="text-primary">Inventory Name</b>, and <b class="text-primary">Inventory Image</b> with JPG, PNG, or JPEG format.</p>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Inventory</b>, after that you will be redirect to Inventory Page.</li>
    <li>At the Inventory Page, press button <b class="text-success">+ Inventory</b>. And it will redirect you to the Add Inventory Page.</li>
    <li>To add an image, you can press <b class="text-success">Add Image</b> button.</li>
    <li>Fill the form and press the <b class="text-success">Save Inventory</b> button to finish.</li>
</ol>
<h3>How to See Your Inventory?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Inventory</b>, after that you will be redirect to Inventory Page.</li>
    <li>Your inventory is displayed in a table format.</li>
</ol>
<h3>How to Edit Your Inventory?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Inventory</b>, after that you will be redirect to Inventory Page.</li>
    <li>Your inventory is displayed in a table format.</li>
    <li>To see the inventory edit form, you can press the button that have "<b class="text-success">Edit</b>" icon. An edit pop-up will appear.</li>
    <li>Change the field that you want to modify. And press the <b class="text-success">Save Changes</b> button to submit changes.</li>
</ol>
<h3>How to Delete Your Inventory?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Inventory</b>, after that you will be redirect to Inventory Page.</li>
    <li>Your inventory is displayed in a table format.</li>
    <li>After you find the inventory that you want to delete. Press the button that have "<b class="text-success">Trash</b>" icon.</li>
    <li>A validation pop-up will appear, press <b class="text-success">Yes, delete it</b> button to finally delete your inventory.</li>
</ol>
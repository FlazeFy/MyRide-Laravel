<h2>Service Module</h2><hr>
<h3>How to Add Service?</h3>
<p>To add your past service history at MyRide Apps you must select your <b class="text-primary">Vehicle</b>, provide <b class="text-primary">Service Category</b>, <b class="text-primary">Total Price</b>, <b class="text-primary">Service Location</b>, 
<b class="text-primary">Remind At</b>, and <b class="text-primary">Service Notes</b>.</p>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Service</b>, after that you will be redirect to Service Page.</li>
    <li>At the Service Page, press button <b class="text-success">+ Service</b>. And it will redirect you to the Add Service Page.</li>
    <li>Fill the form and press the <b class="text-success">Save Service</b> button to finish.</li>
</ol>
<h3>How to See Your Service History?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Service</b>, after that you will be redirect to Service Page.</li>
    <li>Your service history is displayed in a table format.</li>
</ol>
<h3>How to Edit Your Service History?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Service</b>, after that you will be redirect to Service Page.</li>
    <li>Your service history is displayed in a table format.</li>
    <li>To see the service edit form, you can press the button that have "<b class="text-success">Edit</b>" icon. An edit pop-up will appear.</li>
    <li>Change the field that you want to modify. And press the <b class="text-success">Save Changes</b> button to submit changes.</li>
</ol>
<h3>How to Delete Your Service History?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Service</b>, after that you will be redirect to Service Page.</li>
    <li>Your service history is displayed in a table format.</li>
    <li>After you find the service history that you want to delete. Press the button that have "<b class="text-success">Trash</b>" icon.</li>
    <li>A validation pop-up will appear, press <b class="text-success">Yes, delete it</b> button to finally delete your service history.</li>
</ol>
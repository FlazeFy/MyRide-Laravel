<h2>Trip Module</h2><hr>
<h3>How to Add Trip?</h3>
<p>To add your past trip history at MyRide Apps you must select your <b class="text-primary">Vehicle</b>, provide <b class="text-primary">Trip Category</b>, <b class="text-primary">Driver</b>, <b class="text-primary">Description</b>, <b class="text-primary">Person</b>, <b class="text-primary">Trip Origin Name</b>
, <b class="text-primary">Trip Origin Coordinate</b>, <b class="text-primary">Trip Destination Name</b>, and <b class="text-primary">Trip Destination Coordinate</b>.</p>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Trip</b>, after that you will be redirect to Trip Page.</li>
    <li>At the Trip Page, press button <b class="text-success">+ Trip</b>. And it will redirect you to the Add Trip Page.</li>
    <li>Fill the form and select your trip location via Maps or just manually input the coordinate.</li>
    <li>Press the <b class="text-success">Save Trip</b> button to finish.</li>
</ol>
<h3>How to See Your Trip History?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Trip</b>, after that you will be redirect to Trip Page.</li>
    <li>Your trip history is displayed on the map using markers or as a list in a carousel format next to the map.</li>
    <li>To see the trip detail, you can press the button that have "<b class="text-success">Expand</b>" icon. And it will show you the rest of trip information.</li>
    <li>To set direction on your Google Maps, you can press <b class="text-success">Set Route on Maps</b> after you expanded the trip detail.</li>
</ol>
<h3>How to Edit Your Trip History?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Trip</b>, after that you will be redirect to Trip Page.</li>
    <li>Find your trip history in a carousel list displayed next to the map.</li>
    <li>To see the trip edit form, you can press the button that have "<b class="text-success">Edit</b>" icon. An edit pop-up will appear.</li>
    <li>Change the field that you want to modify. And press the <b class="text-success">Save Changes</b> button to submit changes.</li>
</ol>
<h3>How to Delete Your Trip History?</h3>
<ol>
    <li>Open your sidebar and press button <b class="text-success">Trip</b>, after that you will be redirect to Trip Page.</li>
    <li>Find your trip history in a carousel list displayed next to the map.</li>
    <li>After you find the trip history that you want to delete. Press the button that have "<b class="text-success">Trash</b>" icon.</li>
    <li>A validation pop-up will appear, press <b class="text-success">Yes, delete it</b> button to finally delete your trip history.</li>
</ol>